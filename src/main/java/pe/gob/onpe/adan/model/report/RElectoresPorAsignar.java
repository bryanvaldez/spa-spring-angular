package pe.gob.onpe.adan.model.report;

/**
 *
 * @author AQuispec
 */
public class RElectoresPorAsignar {

    private String odpe;
    private String departamento;
    private String provincia;
    private String distrito;
    private String electores;
    private String mesas;

    public String getOdpe() {
        return odpe;
    }

    public void setOdpe(String odpe) {
        this.odpe = odpe;
    }

    public String getDepartamento() {
        return departamento;
    }

    public void setDepartamento(String departamento) {
        this.departamento = departamento;
    }

    public String getProvincia() {
        return provincia;
    }

    public void setProvincia(String provincia) {
        this.provincia = provincia;
    }

    public String getDistrito() {
        return distrito;
    }

    public void setDistrito(String distrito) {
        this.distrito = distrito;
    }

    public String getElectores() {
        return electores;
    }

    public void setElectores(String electores) {
        this.electores = electores;
    }

    public String getMesas() {
        return mesas;
    }

    public void setMesas(String mesas) {
        this.mesas = mesas;
    }

}
